# ================================================================
# crt0.S — C 运行时启动代码（C Runtime 0）
# ================================================================
# 这是 CPU 上电复位后执行的第一段代码，负责：
#   1. 设置堆栈指针 sp（C 函数调用需要栈）
#   2. 设置全局指针 gp（GCC 用 gp 访问全局变量，缩短指令长度）
#   3. 复制 .rodata/.data 段从 ROM 到 RAM（哈佛架构：取指走 ROM，访存走 RAM）
#   4. 清零 .bss 段（未初始化的全局变量必须初始化为 0）
#   5. 调用 main() 进入 C 程序
#
# 内存布局（由 link.ld 定义）：
#   ROM 0x0000_0000 ┌──────────────┐
#                    │ .text        │ 代码
#                    ├──────────────┤
#                    │ .rodata LMA  │ 只读数据在 ROM 中的加载地址（LMA）
#                    │ .data   LMA  │ 已初始化数据在 ROM 中的加载地址
#   ROM 0x0000_0137 ├──────────────┤
#                       ...  ...
#   RAM 0x0000_1000 ┌──────────────┐
#                    │ .rodata VMA  │ 只读数据在 RAM 中的运行地址（VMA）
#                    ├──────────────┤
#                    │ .data   VMA  │ 已初始化数据在 RAM 中的运行地址
#                    ├──────────────┤
#                    │ .bss         │ 未初始化数据（需清零）
#                    ├──────────────┤
#                    │ stack        │ sp 指向这里（栈向下增长）
#   RAM 0x0000_2000 └──────────────┘
#
# 为什么需要复制？LMA vs VMA：
#   编译器生成的数据（如 const char msg[] = "Hello"）编译时存在 ROM。
#   但 CPU 是哈佛架构——指令取指走 ROM，lw/sw 访存走 RAM。
#   程序运行时，lw 指令无法从 ROM 读取数据（addr_decoder 给 rom_bus
#   新增了 ROM 读通路，解决了这个问题，但 VMA 才是数据段最终的运行地址）。
#   所以 crt0.S 要把数据从 ROM LMA 复制到 RAM VMA，后续 C 代码才能用。
#
# 地址来源：
#   下面所有 __xxx 符号由链接脚本（link.ld）根据实际段大小自动计算，
#   不需要手动指定。修改代码后重新链接，地址会自动更新。
# ================================================================

.section .text
.globl _start
.type _start, @function

# ----------------------------------------------------------------
# 入口点：CPU 复位后 PC = 0x00000000，第一条指令从这里开始
# ----------------------------------------------------------------
_start:

    # ============================================================
    # 第 0 步：初始化 sp 和 gp（寄存器，非内存操作）
    # ============================================================

    # .option norelax —— 禁止汇编器"松弛优化"
    # 默认情况下，GNU 汇编器会把 lui+addi 对优化成 gp 相对寻址的一
    # 条指令（如 addi sp, gp, 2028）。但此时 gp 还是复位值 0，优
    # 化后会得到错误的地址。所以先用 norelax 禁止优化，确保 sp 和
    # gp 用绝对地址正确初始化。

    .option push
    .option norelax

    # sp = __stack_top（0x2000，即 0x1000 + 4KB）
    # sp 是栈指针，函数调用时压栈/出栈。栈从高地址向低地址增长。
    lui  sp, %hi(__stack_top)       # sp = 0x2000 的高 20 位
    addi sp, sp, %lo(__stack_top)   # 加上低 12 位（此处低 12 位为 0）

    # gp = __global_pointer$（0x1814，约在 .data 段中间 + 偏移）
    # gp 用于访问全局变量的基址。GCC 生成 gp 相对寻址时，只占 1 条
    # 指令（addi rd, gp, offset），比绝对地址（2 条指令：lui+addi）更省。
    la   gp, __global_pointer$      # 展开为 auipc+addi 对

    # ============================================================
    # 第 1 步：复制 .rodata 段（ROM LMA → RAM VMA）
    # ============================================================
    # 只读数据如 const char[]、printf 格式串等，编译时存在 ROM 中。
    # 运行时要复制到 RAM，否则 lw 指令访问不到。
    #
    # 寄存器约定：
    #   a0 = 源地址（ROM LMA）
    #   a1 = 目标地址（RAM VMA）
    #   a2 = 结束地址（RAM VMA + 段大小）
    #   a3 = 临时搬运寄存器（每次搬运 4 字节）

    la   a0, __rodata_load_start    # a0 = .rodata 段在 ROM 中的起始地址
    la   a1, __rodata_start         # a1 = .rodata 段在 RAM 中的起始地址
    la   a2, __rodata_end           # a2 = .rodata 段在 RAM 中的结束地址
    .option pop                      # 恢复汇编器默认优化行为

1:  bge  a1, a2, 2f                 # if (a1 >= a2) 复制完成，跳到标签 2
    lw   a3, 0(a0)                  # a3 = ROM[a0]（读 4 字节）
    sw   a3, 0(a1)                  # RAM[a1] = a3  （写 4 字节）
    addi a0, a0, 4                  # 源地址 +4（下一个 32-bit word）
    addi a1, a1, 4                  # 目标地址 +4
    j    1b                         # 继续循环（"1b" = 向后找最近的标签 1）

    # ============================================================
    # 第 2 步：复制 .data 段（ROM LMA → RAM VMA）
    # ============================================================
    # .data 段包含已初始化的全局变量，如 int x = 42。
    # 原理与 .rodata 完全相同：ROM → RAM 复制。
    # 本例中 .data 段为空（__data_load_start == __data_start==__data_end），
    # 所以这个循环会被跳过。但保留代码，后续添加全局变量时会自动生效。

2:  la   a0, __data_load_start       # a0 = .data 段在 ROM 中的起始地址
    la   a1, __data_start            # a1 = .data 段在 RAM 中的起始地址
    la   a2, __data_end              # a2 = .data 段在 RAM 中的结束地址
3:  bge  a1, a2, 4f                  # if (a1 >= a2) 跳到标签 4
    lw   a3, 0(a0)
    sw   a3, 0(a1)
    addi a0, a0, 4
    addi a1, a1, 4
    j    3b

    # ============================================================
    # 第 3 步：清零 .bss 段
    # ============================================================
    # C 标准要求未初始化的全局变量（如 static int x; 或 int y;）必
    # 须为 0。编译器把这些变量放在 .bss 段，但 ELF 文件中 .bss 不
    # 占实际空间（只记录大小）。所以 crt0.S 必须在运行时把这块 RAM
    # 清零。
    #
    # 同样，本例中 .bss 为空，循环会被跳过。

4:  la   a1, __bss_start             # a1 = .bss 段起始地址
    la   a2, __bss_end               # a2 = .bss 段结束地址
5:  bge  a1, a2, 6f                  # if (a1 >= a2) 跳到标签 6
    sw   zero, 0(a1)                 # RAM[a1] = 0（x0 恒为 0）
    addi a1, a1, 4
    j    5b

    # ============================================================
    # 第 4 步：初始化 trap 和中断
    # ============================================================
    # 设置 mtvec = trap_entry (trap handler 入口)
    # 使能外部中断 (mie[11]=1) 和全局中断 (mstatus[3]=1)
    # ============================================================

    la    t0, trap_entry
    csrw  mtvec, t0               # mtvec = &trap_entry

    li    t0, 0x880               # mie: bit11=外部中断, bit7=Timer, bit3=软件
    csrw  mie, t0                 # 使能外部中断 (MEIE)

    li    t0, 0x8                 # mstatus: bit3=MIE (中断总开关)
    csrs  mstatus, t0             # 开全局中断

    # ============================================================
    # 第 5 步：进入 C 程序
    # ============================================================
    # 至此 sp、gp 已就绪，.rodata/.data 已复制到 RAM，.bss 已清零。
    # 调用 main() 进入用户 C 代码。main 返回后死循环（当前无 OS，
    # 无法真正退出。后续实现 _exit=ECALL→machine mode 关机）。

6:  call main                         # 调用 main()，同时保存 ra=返回地址
    j    6b                          # main 返回后在此死循环
